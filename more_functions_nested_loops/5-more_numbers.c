#include "main.h"

/**
 * more_numbers - print all 10 digits
 * Return: result
 */
void more_numbers(void)
{
	int n = 48;
	int m;
	int dizaine = 0;

	for (m = 0 ; m <= 14 ; m++)
	{
		if (dizaine = 1)
		{
			_putchar(49);
		}
		_putchar(n);
		if (n >= 57)
		{
			n = 48;
			dizaine = 1;
		}
		
	}
	_putchar('\n');
}
