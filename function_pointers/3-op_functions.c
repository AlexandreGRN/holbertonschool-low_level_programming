#include "3-calc.h"

/**
 * op_add - add 2 int
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub 2 int
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul 2 int
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div 2 int
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod 2 int
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}

